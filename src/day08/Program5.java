package day08;

public class Program5 {public static void main(String[] args) {
	// < > <= >= == !=  true/false
	int a=10;
	int b=2;
	
	System.out.println(a>b);//true
	System.out.println(a<b);//false
	System.out.println(a>=b);//true
	System.out.println(a<=b);//false
	System.out.println(a==b);//false
	System.out.println(a!=b);//true
}

}
